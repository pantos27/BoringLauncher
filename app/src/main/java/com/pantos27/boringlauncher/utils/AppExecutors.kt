package com.pantos27.boringlauncher.utils

import android.os.Handler
import android.os.Looper
import android.support.annotation.VisibleForTesting

import java.util.concurrent.Executor
import java.util.concurrent.Executors

/**
 * Global executor pools for the whole application.
 *
 *
 * Grouping tasks like this avoids the effects of task starvation (e.g. disk reads don't wait behind
 * webservice requests).
 */
object AppExecutors{
    private const val THREAD_COUNT = 3
    public val networkIO: Executor by lazy { Executors.newFixedThreadPool(THREAD_COUNT) }
    public val diskIO: Executor by lazy { DiskIOThreadExecutor() }
    public val mainThread: Executor by lazy { MainThreadExecutor() }

    private class MainThreadExecutor : Executor {
        private val mainThreadHandler = Handler(Looper.getMainLooper())

        override fun execute(command: Runnable) {
            mainThreadHandler.post(command)
        }
    }

}
